function humanizeTaxonomyId(id) {
  return String(id || "")
    .replace(/_/g, " ")
    .replace(/\b\w/g, (char) => char.toUpperCase());
}

export function getCraftPlannerTaxonomyLabel(kind, id, language) {
  if (!id) return "";
  const scopedKind = kind === "category" ? "category" : "subcategory";
  const locale =
    craftPlannerTaxonomyLabels[language] || craftPlannerTaxonomyLabels["EN-US"];

  return (
    locale?.[scopedKind]?.[id] ||
    craftPlannerTaxonomyLabels["EN-US"]?.[scopedKind]?.[id] ||
    humanizeTaxonomyId(id)
  );
}

